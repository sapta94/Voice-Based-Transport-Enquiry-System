package com.vtes.model;

public class CabInfo {
	private String cabtype;
	private String desc;
	private String cpacity;
	private String cpm;
	private String cpd;
	private String cf;
	public String getCabtype() {
		return cabtype;
	}
	public void setCabtype(String cabtype) {
		this.cabtype = cabtype;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getCpacity() {
		return cpacity;
	}
	public void setCpacity(String cpacity) {
		this.cpacity = cpacity;
	}
	public String getCpm() {
		return cpm;
	}
	public void setCpm(String cpm) {
		this.cpm = cpm;
	}
	public String getCpd() {
		return cpd;
	}
	public void setCpd(String cpd) {
		this.cpd = cpd;
	}
	public String getCf() {
		return cf;
	}
	public void setCf(String cf) {
		this.cf = cf;
	}
	
}
